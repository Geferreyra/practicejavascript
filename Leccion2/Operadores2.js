let a =10 , b=3;
let z = a +b;

//Incremento

//PRE-incremento(operador ++ antes de la variable)

z=++a;


console.log(a);
console.log(z);

//POST-incremento(operador ++ despues de la variable)

z=b++

console.log(b);
console.log(z);


//Decremento
//Predecremento
z= --a;
console.log(a);
console.log(z);

//Postdecremento

z=b--;

console.log(b);
console.log(z);

